#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	size;

	if (!lst)
		return (0);
	size = 0;
	while (lst != NULL && size >= 0)
	{
		lst = lst->next;
		size++;
	}
	return (size);
}